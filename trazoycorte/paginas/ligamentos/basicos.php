			2.3.3 - SATÍN Ó RASO:



			Este tipo de ligamento es muy flojo por lo tanto se utiliza para telas muy suaves y delicadas , en la costura se puede deshilar muy fácilmente. Se llama satín cuando se teje con seda y raso cuando se teje con algodón. Ligamento fundamental cuyos puntos de ligadura o unión quedan separados uno de otros. Este tejido es muy poco consistente y se puede ir fácilmente por las costuras.

