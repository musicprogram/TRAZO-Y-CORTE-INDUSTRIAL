		<li class="list-group-item">-2.2.2 - Según su estructura física en: Telas tejidas y no tejidas <br>

			Telas tejidas: <br>

			Tejido plano: es el resultado del entrelazamiento de los hilos de urdimbre y los hilos de trama: Dril, dacrón, satín, coleta, etc. <br>
			Tejido de punto: Es el entrelazamiento de uno o una serie de hilos en si mismo, en forma de bucles que nos dan como resultado un tejido de gran elasticidad. Ejemplo: El rib, el punto sencillo, acanalado etc. <br>
			Telas no tejidas o aglutinados : <br>

			Son aquellas fibras que por medio de un aglutinamiento, una presión y temperatura sometida a fusión ó químico adhesivo forman la tela : Ejemplo: La entretela.telas para pañal desechable, etc.
